public class Conversor {

    public static double metrosAKilometros(double valor){
        return valor / 1000;
    }

    public static double kilometrosAMillas(double valor){
        return valor * 0.62137;
    }

    public static double millasAKilometros(double valor){
        return valor * 1.60934;
    }

    public static double metrosAYardas(double valor){
        return valor * 1.09361;
    }
}